void initSSD2828_start()
{
    writec16(0x00B6);//Video Mode & Pixel Format
    writed16(0x0003);
    
    writec16(0x00B7);
    writed16(0x0050);
    
    writec16(0x00B8);
    writed16(0x0000);
    
    writec16(0x00B9);
    writed16(0x0000);
    
    
    writec16(0x00Ba);
    writed16(0x4214);
    
    writec16(0x00BB);
    writed16(0x0005);//03
    
    writec16(0x00B9);
    writed16(0x0001);
    
    
    writec16(0x00DE);
    writed16(0x0000);
    
    writec16(0x00C9);
    writed16(0x2302);
    mdelay(100);


}

void initSSD2828_end()
{
    WriteRegister(0x00b7,0x0050);
    WriteRegister(0x00b8,0x0000);
    WriteRegister(0x00b9,0x0000);
    //WriteRegister(0x00bA,0x8228);//8222  8228
    WriteRegister(0x00bA,0x8228);//8222  8228
    WriteRegister(0x00bB,0x0007);
    WriteRegister(0x00b9,0x0001);
    WriteRegister(0x00C9,0x2302);
    mdelay(10);
    WriteRegister(0x00CA,0x2301);
    WriteRegister(0x00CB,0x0510);
    WriteRegister(0x00CC,0x1005);
    WriteRegister(0x00b1,S5PV210_LCD_VSPW << 8 | S5PV210_LCD_HSPW);//0x0503    
    WriteRegister(0x00b2,S5PV210_LCD_VBPD << 8 | S5PV210_LCD_HBPD);//0x192d
    WriteRegister(0x00b3,S5PV210_LCD_VFPD << 8 | S5PV210_LCD_HFPD);//0x0f1e
    WriteRegister(0x00b4,0x02D0);
    WriteRegister(0x00b5,0x0500);
    WriteRegister(0x00b6,0X001B);
    WriteRegister(0x00DE,0x0003);//03
    WriteRegister(0x00D6,0x0005);
    WriteRegister(0x00b7,0x024B);//1111111

}


